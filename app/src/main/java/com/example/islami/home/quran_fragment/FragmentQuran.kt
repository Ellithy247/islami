    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.fragment_quran, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)


        suraNamesRecyclerView = view.findViewById<RecyclerView>(R.id.suraNamesRecyclerView)
        suraNamesAdapter = SuraNamesAdapter(suraNamesList)
        suraNamesAdapter.onSuraNameClicked = object : SuraNamesAdapter.OnItemClickListner{
            override fun onItemClikListner(@NotNull item: String, index: Int) {
                startSuraNameScreen(item,"${index+1}.txt")
            }
        }
        suraNamesRecyclerView.adapter = suraNamesAdapter



    }

    private fun startSuraNameScreen(suraname:String,filename :String) {

        val intent : Intent = Intent(activity,sura_details_activity::class.java)
        intent.putExtra("sura_name",suraname)
        intent.putExtra("file_name",filename)

        startActivity(intent)

    }
}